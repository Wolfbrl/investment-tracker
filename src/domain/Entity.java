package domain;

public class Entity {

	private String symbol;
	private String name;
	private String type;
	private String industry;

	public Entity() {
	}

	public String getSymbol() {
		return symbol;
	}

	public String getName() {
		return name;
	}

	public String getType() {
		return type;
	}

	public String getIndustry() {
		return industry;
	}
}
